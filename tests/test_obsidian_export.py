def test_get_note_path(tmp_path):
    """Test note path generation and sanitization."""
    config = ObsidianConfig(
        vault_path=str(tmp_path),
        ai_reports_folder="Reports",
        reviews_folder="Log/Reviews"
    )
    date = datetime(2026, 4, 11)
    
    # AI Report path
    path = get_note_path(config, "standup", date, "Daily Standup")
    assert path == tmp_path / "Reports" / "2026-04-11 — Daily Standup.md"
    
    # Review path
    path = get_note_path(config, "review", date, "5-Day Review")
    assert path == tmp_path / "Log/Reviews" / "2026-04-11 — 5-Day Review.md"
    
    # Sanitization
    path = get_note_path(config, "standup", date, "Project: Work/Life?")
    assert "Project WorkLife" in path.name
    assert ":" not in path.name
    assert "/" not in path.name
    assert "?" not in path.name


def test_write_note(tmp_path):
    """Test atomic note writing."""
    target_path = tmp_path / "subdir" / "note.md"
    content = "Hello Obsidian"
    
    write_note(target_path, content)
    
    assert target_path.exists()
    assert target_path.read_text(encoding="utf-8") == content
    assert not target_path.with_suffix(".tmp.md").exists()


def test_obsidian_is_configured(tmp_path):
    """Test configuration validation."""
    # Not set
    config = ObsidianConfig(vault_path=None)
    assert not obsidian_is_configured(config)
    
    # Nonexistent
    config = ObsidianConfig(vault_path=str(tmp_path / "missing"))
    assert not obsidian_is_configured(config)
    
    # Valid
    config = ObsidianConfig(vault_path=str(tmp_path))
    assert obsidian_is_configured(config)

