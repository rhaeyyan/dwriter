def test_strip_rich_markup():
    """Test stripping Rich markup while preserving Markdown and wikilinks."""
    assert strip_rich_markup("[#23c76b]text[/#23c76b]") == "text"
    assert strip_rich_markup("[bold #cba6f7]text[/]") == "text"
    assert strip_rich_markup("[dim]text[/dim]") == "text"
    assert strip_rich_markup("Regular text") == "Regular text"
    
    # Preserve wikilinks
    assert strip_rich_markup("[[wikilink]]") == "[[wikilink]]"
    assert strip_rich_markup("Check [[ProjectX]] for details") == "Check [[ProjectX]] for details"
    
    # Preserve hashtags
    assert strip_rich_markup("#hashtag") == "#hashtag"
    assert strip_rich_markup("Fixing #bug in &dwriter") == "Fixing #bug in &dwriter"
    
    # Compound and block characters
    assert strip_rich_markup("[bold #a6e3a1]█░[/]") == "█░"


def test_render_ai_report_note():
    """Test AI report note rendering with YAML frontmatter."""
    title = "Weekly Retro"
    report_kind = "weekly-retro"
    content = "This was a good week."
    date = datetime(2026, 4, 11, 14, 30)
    
    note = render_ai_report_note(title, report_kind, content, date)
    
    # Basic structure
    assert note.startswith("---")
    assert content in note
    assert "# Weekly Retro" in note
    assert "Generated by dwriter 2nd-Brain on 2026-04-11 at 14:30" in note
    
    # Check frontmatter strings directly to avoid YAML dependency in tests
    assert "date: 2026-04-11" in note
    assert "type: ai-report" in note
    assert "report-kind: weekly-retro" in note
    assert "source: dwriter-2nd-brain" in note


def test_render_ai_report_note_with_range():
    """Test AI report note rendering with range label (Catch Up)."""
